<div class=\"col-md-8\">
<p>A company is a separate legal corporate entity and is operated by legal rules and procedures given under the Companies Act 2013.

ROC is also known as Registrar of Companies under the Ministry of Corporate Affairs looks after the compliances of Companies under its jurisdiction. Every Pvt. Ltd. company irrespective of its size has to file returns and documents to comply with the legal requirements given in the Act and is known as ROC compliances.

Failing to comply with rules & guidelines can attract a hefty penalty on the company and their stakeholders</p>
<p><strong>So Let's get insights on what are the key components of annual compliances </strong></p>
</div>

<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Filing of DIR- 8 </a>
<p>As per Section 164(2) of Company's Act 2013 Every Director of the Company in each Financial Year will file with the Company disclosure and non-disqualification.</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Filing of MBP-1 </a>
<p>As per Section 184(1) of Company's Act 2013 Every Director of the Company in First Meeting of the Board of Director in each Financial Year will disclose his interest in other entities under (Form MBP-1)</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Filing of MGT-7</a>
<p>As per Section 92 of Company Act 2013, Every Company will file its E-form also known as Annual Return within 60 days of holding MGT-7 Annual General Meeting. Annual Return will be for the period 1 st April to 31st March.</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Filing of AOC-4</a>
<p>As per Section 137 of Company Act 2013, Every Company is required to file its Balance Sheet along with a statement of Profit and Loss Account and Director Report in this form.</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Preparation & Filing of Financial Statements along with Form AOC-4</a>
<p>Balance Sheet, Statement of Profit & Loss Account (Including Consolidated Financial Statement), Directors’ Report, Auditors’ Report, Cash Flow Statement and Notice of AGM.</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Preparation of Director Report</a>
<p>As per Section 134 of Company Act 2013, Directors’ Report will be prepared by a mention of all the information required for Small Company.</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Circulation of Financial Statement </a>
<p>As per Section 136 of Company Act 2013, Company will send to the members of the Company approved Financial Statement (including consolidated Financial Statement), Cash Flow Statement, Directors’ Report and Auditors’ Report at least 21 clear days before the Annual General Meeting. (Except in case of AGM is called on Shorter Notice)</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Scheduling of Annual General Meeting</a>
<p>As per Section 136 of Company Act 2013, Every Notice of Annual General Meeting will be prepared SS-II as per Section 101 of Companies Act 2013 and Secretarial Standard – II.</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Scheduling Board Meetings</a>
<p>As Per Section 173 of Company Act 2013 & Secretarial Standard – I, Every Company shall hold a minimum number of FOUR meetings of its Board of Directors every year in such a manner that maximum gap between two meeting should not be more than 120 (One hundred twenty) days. The company should hold at least 1 (one) Board Meeting every quarter of the calendar year.</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Filing of ADT-1</a>
<p>As per Section 139 of Company Act 2013, Every company needs to appoint of Auditor will be appointed for the 5 (Five) years and form ADT-1 will be filed for a 5-years appointment.

After that, every year in AGM shareholder will ratify the Auditor but there is no need to file ADT-1.</p>
</div>
<div class=\"col-md-8\">
<a href=\"#\"><i class=\"material-icons mdi mdi-bookmark-outline\"></i>Preparation & Filing of Annual Return</a>
<p>As per Section 92 of Company Act 2013, Annual Return of Private Company (Except Small Company) should be signed by Company Secretary in Practice.</p>
</div>
